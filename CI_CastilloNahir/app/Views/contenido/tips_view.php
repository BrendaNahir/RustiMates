		<p style="text-align: justify;">2. Vierta yerba dentro del mate hasta alcanzar las tres cuartas partes del mismo.</p>

		<p style="text-align: justify;">3. Tape con una mano la boca del mate, inviértalo y agítelo unos instantes. Vuelva el objeto a su posición normal. Cuide que la yerba quede recostada sobre una de las paredes del mate y además se forme un pequeño agujero.</p>

		<p style="text-align: justify;">4. Para comenzar el mate, coloque agua tibia/fria suavemente en el hueco que quedó en la yerba (permitirá que no se queme la yerba y pierda el gusto rápidamente).</p>

		<p style="text-align: justify;">5. Introduzca la bombilla en el hueco húmedo.</p>

		<p style="text-align: justify;">6. Procure no mover la bombilla y cebar siempre en el mismo lugar, esto permitirá que no moje toda la yerba y así disfrutará más tiempo de unos exquisitos mates.</p></strong>
