    <div id="collapseTwo" class="accordion-collapse collapse" aria-labelledby="headingTwo" data-bs-parent="#accordionExample">
      <div class="accordion-body">
        <strong><p style="text-align: justify;">1. Lavá bien el interior del mate con agua caliente.
		<p style="text-align: justify;">2. Llenalo con yerba húmeda ya usada previamente, agregale agua tibia para que la yerba no se seque y dejá reposar.</p>
		<p style="text-align: justify;">3. Al día siguiente, quitá la yerba y con una cucharita raspá el interior para quitar el hollejo, las membranas o los fragmentos del tallo leñoso.</p>
		<p style="text-align: justify;">4. Volvé a repetir la técnica desde el comienzo, aunque sin lavar el mate.
		Tres veces va a ser suficiente para que el mate quede bien curado.</p></strong> 
