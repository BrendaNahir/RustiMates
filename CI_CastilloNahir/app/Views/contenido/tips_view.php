    <p style="text-align: justify;">2. No dejar el mate con yerba usada más de 24 hs.</p>
    <p style="text-align: justify;">3. Únicamente lavarlo con un poco de agua, sólo la parte de la calabaza, para sacarle los restos de yerba. Evitando así que la yerba que queda sobre el mate no se descomponga.</p>
    <p style="text-align: justify;">4. No dejarlo secar boca abajo, menos cuando tiene un poco de líquido dentro, ya que éste caerá sobre la virola y colaborará con una posible oxidación.
    </p>
    <p style="text-align: justify;">5. Evitar mojar el cuero que recubre la calabaza y dejarlo al sol directo. Éstos dos provocan que se reseque y se agriete el cuero.</p>
    <p style="text-align: justify;">6. Evitar golpes, para que la virola no se afloje y para que no se quiebre la calabaza.</p>
    </strong> 
